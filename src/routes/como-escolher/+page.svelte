<div class="flex flex-col items-center justify-center min-h-30">
    <h1 class="font-bold text-2xl ">
        Como escolher o brinquedo ideal
    </h1>
    <span>Escolher o brinquedo adequado para cada faixa etária faz parte do cuidado com o desenvolvimento da criança. 
        Brinquedos bem selecionados podem estimular habilidades motoras, criatividade, imaginação, habilidades sociais 
        e resolução de problemas, contribuindo para o crescimento saudável em diferentes fases da infância.</span>
</div>
<div class="flex flex-col items-center justify-center min-h-30">
    <span>A indicação de idade presente nas embalagens existe para ajudar a encontrar brinquedos que sejam apropriados ao 
        estágio de desenvolvimento de cada criança. Essa recomendação leva em conta habilidades motoras, cognitivas e de interação da faixa etária.</span>
</div>

<div class="flex flex-col items-center justify-center min-h-45 ">
    <div class="grid grid-cols-2 gap-3 text-center">
        <span>Segurança deve ser prioridade na escolha. Verifique se o brinquedo não possui partes pequenas que possam ser engolidas por crianças pequenas ou peças pontiagudas.
             Certificações de segurança como selos de conformidade ajudam a identificar produtos mais confiáveis. Além disso, prefira materiais não tóxicos e fáceis de limpar.</span>
        <span>Tente escolher brinquedos que incentivem:
            <ul><li></li></ul>
            Coordenação motora (como blocos de montar ou brinquedos de encaixe)

            Criatividade e imaginação (como jogos de faz-de-conta ou materiais de artes)

            Interação social (como brincadeiras em grupo ou jogos simples)
            Brinquedos que permitem múltiplas formas de brincar ajudam a manter o interesse da criança e podem se adaptar ao seu crescimento.
        </span>
        <span>Brinquedos simples e abertos — como blocos, formas para empilhar, jogos de encaixe ou peças de faz-de-conta — costumam favorecer a imaginação e permitem que a criança explore sem depender de estímulos eletrônicos ou respostas automáticas.</span>
        <span>Cada criança é única e pode ter ritmos diferentes de desenvolvimento. Observar como ela interage com os brinquedos ajuda a identificar interesses, desafios e habilidades que podem orientar escolhas futuras.</span>
    </div>
    
</div>
